class Person:
    def __init__(self, name, age):
        self.name = name
        self.age = age
    
    def introduce(self):
        print(f"Xin chào, tôi là {self.name} và tôi {self.age} tuổi.")

# Tạo một instance của lớp Person
person1 = Person("Alice", 25)

